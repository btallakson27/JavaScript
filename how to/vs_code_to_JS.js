// To add changes from VS Code to Github.

// 1. Access terminal. 
// 2. Git init the master folder with everything in it.
// 3. Cd to get to the most immediate folder containing the folder or file you want to update. cd .. moves you up a folder.
// 4. Git add . to update everything or get add file_name/ to update specific file or folder
// 5. Git commit -am, “personal note of update”
// 6. Git push origin main 